public class CoachModel {
    private String coachName;
    private String coachTeachTime;
    private String coachPoints;
    private String coachActualPrice;
    private String coachOldPrice;

    public String getCoachName() {
        return coachName;
    }

    public void setCoachName(String coachName) {
        this.coachName = coachName;
    }

    public String getCoachTeachTime() {
        return coachTeachTime;
    }

    public void setCoachTeachTime(String coachTeachTime) {
        this.coachTeachTime = coachTeachTime;
    }

    public String getCoachPoints() {
        return coachPoints;
    }

    public void setCoachPoints(String coachPoints) {
        this.coachPoints = coachPoints;
    }

    public String getCoachActualPrice() {
        return coachActualPrice;
    }

    public void setCoachActualPrice(String coachActualPrice) {
        this.coachActualPrice = coachActualPrice;
    }

    public String getCoachOldPrice() {
        return coachOldPrice;
    }

    public void setCoachOldPrice(String coachOldPrice) {
        this.coachOldPrice = coachOldPrice;
    }
}
